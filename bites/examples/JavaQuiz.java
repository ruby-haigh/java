package bites.examples;

public class JavaQuiz {
    public static void main(String[] args) {
        System.out.println("Q: Does all Java code have to be inside a class?");
        System.out.println("A: Yes");
        System.out.println("Q: What is compilation?");
        System.out.println("A: The process by which java code is translated into bytecode");
        System.out.println("Q: What is special about the main method?");
        System.out.println("A: It is always executed. You need one per program (and only one)");
        System.out.println("Q: What does void mean?");
        System.out.println("A: That the method doesn't return anything");

    }
}
